def generate_all_content(
    systems,
    edges,
    src_dir,
    model,
    no_cache,
    max_workers,
    agent_routes,
    agent_invocations,
):
    """Generate all content in parallel with caching."""
    print("Phase 3: Generating system descriptions...")
    sys_results = {}

    with ThreadPoolExecutor(max_workers=max_workers) as pool:
        futures = {}
        for sid, data in systems.items():
            future = pool.submit(generate_system_content, sid, data, src_dir, model, no_cache)
            futures[future] = sid

        for future in as_completed(futures):
            sid = futures[future]
            try:
                result = future.result()
                if result:
                    sys_results[sid] = result
            except Exception as exc:
                print(f"  ERROR generating {sid}: {exc}")

    print(f"  Generated {len(sys_results)}/{len(systems)} system descriptions")

    print("Phase 4: Generating edge descriptions...")
    edge_results = {}

    with ThreadPoolExecutor(max_workers=max_workers) as pool:
        futures = {}
        for idx, edge in enumerate(edges):
            future = pool.submit(generate_edge_content, edge, systems, src_dir, model, no_cache)
            futures[future] = idx

        for future in as_completed(futures):
            idx = futures[future]
            try:
                result = future.result()
                if result:
                    edge_results[idx] = result
            except Exception as exc:
                print(f"  ERROR generating edge {idx}: {exc}")

    print(f"  Generated {len(edge_results)}/{len(edges)} edge descriptions")

    print("Phase 4b: Generating agent descriptions...")
    agent_results = {}
    agent_tasks = []
    for sid, data in systems.items():
        for agent in data.get("agents", []):
            agent_name = agent["name"]
            agent_basename = f"{agent_name}.md"
            routes = agent_routes.get(agent_basename, [])
            invocs = agent_invocations.get(agent_basename, [])
            agent_tasks.append((agent, sid, routes, invocs))

    with ThreadPoolExecutor(max_workers=max_workers) as pool:
        futures = {}
        for agent, sid, routes, invocs in agent_tasks:
            future = pool.submit(generate_agent_content, agent, sid, routes, invocs, src_dir, model, no_cache)
            futures[future] = f"{sid}.{agent['name']}"

        for future in as_completed(futures):
            key = futures[future]
            try:
                result = future.result()
                if result:
                    agent_results[key] = result
            except Exception as exc:
                print(f"  ERROR generating agent {key}: {exc}")

    print(f"  Generated {len(agent_results)}/{len(agent_tasks)} agent descriptions")

    return sys_results, edge_results, agent_results


