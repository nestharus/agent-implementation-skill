def pick_reference_ids(
    refs: list[dict],
    *,
    count: int = 2,
    suffixes: tuple[str, ...] = (),
    contains: tuple[str, ...] = (),
) -> list[str]:
    if not refs:
        return []

    preferred = []
    for ref in refs:
        path = ref["path"]
        if suffixes and any(path.endswith(suffix) for suffix in suffixes):
            preferred.append(ref)
            continue
        if contains and any(token in path for token in contains):
            preferred.append(ref)

    chosen = []
    seen = set()
    for ref in preferred + refs:
        if ref["id"] in seen:
            continue
        chosen.append(ref["id"])
        seen.add(ref["id"])
        if len(chosen) >= count:
            break
    return chosen


