def parse_agent_frontmatter(md_path):
    """Extract lightweight agent metadata from agent .md frontmatter."""
    metadata = {
        "description": md_path.stem,
        "model": "",
    }
    try:
        text = md_path.read_text(encoding="utf-8", errors="replace")
        if not text.startswith("---"):
            return metadata
        end = text.index("---", 3)
        front = text[3:end]
        for line in front.splitlines():
            stripped = line.strip()
            if stripped.startswith("description:"):
                metadata["description"] = stripped.split(":", 1)[1].strip().strip("'\"")
            elif stripped.startswith("model:"):
                metadata["model"] = stripped.split(":", 1)[1].strip().strip("'\"")
        return metadata
    except Exception:
        return metadata


def module_display_label(module_name: str) -> str:
    if module_name == "core":
        return "Core"
    if module_name == "agents":
        return "Agents"
    return module_name.replace("_", " ").title()


def module_sort_key(module_name: str) -> tuple[int, str]:
    if module_name == "core":
        return (0, module_name)
    if module_name == "agents":
        return (2, module_name)
    return (1, module_name)


def summarize_agent(agent: dict) -> str:
    description = (agent.get("description") or "").strip()
    model = (agent.get("model") or "").strip()
    if description and model:
        return f"{description} Model: {model}."
    if description:
        return description
    if model:
        return f"Model: {model}."
    return "Agent definition."


def discover_system_modules(src_dir: Path, system_id: str, system_dir: Path, agents: list[dict]) -> dict[str, dict]:
    modules: dict[str, dict] = {}

    top_level_py = sorted(
        str(path.relative_to(src_dir))
        for path in system_dir.glob("*.py")
    )
    if top_level_py:
        modules["core"] = {
            "id": "core",
            "kind": "core",
            "label": module_display_label("core"),
            "py_files": top_level_py,
            "md_files": [],
            "all_files": list(top_level_py),
            "agentCount": 0,
        }

    for entry in sorted(system_dir.iterdir()):
        if not entry.is_dir():
            continue
        if entry.name.startswith(".") or entry.name in EXCLUDED_DIRS:
            continue
        if entry.name == "agents":
            continue

        py_files = sorted(str(path.relative_to(src_dir)) for path in entry.rglob("*.py"))
        non_trivial_py = [path for path in py_files if Path(path).name != "__init__.py"]
        if not non_trivial_py:
            continue

        modules[entry.name] = {
            "id": entry.name,
            "kind": "module",
            "label": module_display_label(entry.name),
            "py_files": py_files,
            "md_files": [],
            "all_files": list(py_files),
            "agentCount": 0,
        }

    agents_dir = system_dir / "agents"
    if agents_dir.exists():
        md_files = sorted(str(path.relative_to(src_dir)) for path in agents_dir.glob("*.md"))
        if md_files:
            modules["agents"] = {
                "id": "agents",
                "kind": "agents",
                "label": module_display_label("agents"),
                "py_files": [],
                "md_files": md_files,
                "all_files": list(md_files),
                "agentCount": len(agents),
            }

    return dict(sorted(modules.items(), key=lambda item: module_sort_key(item[0])))


def python_module_name_for_file(rel_path: str) -> str:
    path = Path(rel_path)
    parts = list(path.with_suffix("").parts)
    if parts and parts[-1] == "__init__":
        parts = parts[:-1]
    return ".".join(parts)


def resolve_importfrom_module_name(node: ast.ImportFrom, source_rel_path: str) -> str:
    if not node.level:
        return node.module or ""
    current_module = python_module_name_for_file(source_rel_path)
    current_parts = current_module.split(".") if current_module else []
    if Path(source_rel_path).name != "__init__.py" and current_parts:
        current_parts = current_parts[:-1]
    base_parts = list(current_parts)
    if node.level:
        keep = max(len(current_parts) - (node.level - 1), 0)
        base_parts = current_parts[:keep]
    if node.module:
        return ".".join(part for part in [*base_parts, node.module] if part)
    return ".".join(base_parts)


def file_module_bucket(rel_path: str, modules: dict[str, dict]) -> str | None:
    path = Path(rel_path)
    if len(path.parts) < 2:
        return None
    if len(path.parts) == 2 and path.suffix == ".py":
        return "core" if "core" in modules else None
    module_name = path.parts[1]
    if module_name in modules:
        return module_name
    return None


def resolve_internal_import_targets(
    node: ast.AST,
    source_rel_path: str,
    system_id: str,
    modules: dict[str, dict],
    core_file_stems: set[str],
) -> set[str]:
    targets: set[str] = set()

    def resolve_module_name(module_name: str | None, *, names: list[str] | None = None) -> None:
        if module_name == system_id:
            for name in names or []:
                if name in modules:
                    targets.add(name)
                elif name in core_file_stems and "core" in modules:
                    targets.add("core")
            return
        if not module_name or not module_name.startswith(system_id + "."):
            return
        parts = module_name.split(".")
        if len(parts) < 2:
            return
        candidate = parts[1]
        if candidate in modules:
            targets.add(candidate)
        elif candidate in core_file_stems and "core" in modules:
            targets.add("core")

    if isinstance(node, ast.ImportFrom):
        module_name = resolve_importfrom_module_name(node, source_rel_path)
        resolve_module_name(module_name, names=[alias.name for alias in node.names])
        if not node.module:
            for alias in node.names:
                if alias.name in core_file_stems and "core" in modules:
                    targets.add("core")
                elif alias.name in modules:
                    targets.add(alias.name)
        return targets

    if isinstance(node, ast.Import):
        for alias in node.names:
            resolve_module_name(alias.name)
    return targets


def discover_internal_edges(src_dir: Path, system_id: str, modules: dict[str, dict]) -> list[dict]:
    edge_counts: dict[tuple[str, str], int] = defaultdict(int)
    core_file_stems = {
        Path(rel_path).stem
        for rel_path in modules.get("core", {}).get("py_files", [])
        if Path(rel_path).stem not in {"__init__"}
    }

    for module_name, module_data in modules.items():
        for rel_path in module_data.get("py_files", []):
            try:
                source = (src_dir / rel_path).read_text(encoding="utf-8", errors="replace")
                tree = ast.parse(source)
            except Exception:
                continue
            source_bucket = file_module_bucket(rel_path, modules)
            if not source_bucket:
                continue
            for node in ast.walk(tree):
                for target_bucket in resolve_internal_import_targets(
                    node,
                    rel_path,
                    system_id,
                    modules,
                    core_file_stems,
                ):
                    if target_bucket == source_bucket:
                        continue
                    edge_counts[(source_bucket, target_bucket)] += 1

    return [
        {"from": source, "to": target, "count": count}
        for (source, target), count in sorted(edge_counts.items())
    ]


def build_python_module_lookup(files: list[str]) -> dict[str, str]:
    lookup: dict[str, str] = {}
    for rel_path in files:
        if Path(rel_path).suffix != ".py":
            continue
        lookup[python_module_name_for_file(rel_path)] = rel_path
    return lookup


def resolve_internal_import_file_targets(
    node: ast.AST,
    source_rel_path: str,
    system_id: str,
    module_lookup: dict[str, str],
) -> set[str]:
    targets: set[str] = set()

    def add_candidate(module_name: str | None) -> None:
        if not module_name:
            return
        if module_name != system_id and not module_name.startswith(system_id + "."):
            return
        rel_path = module_lookup.get(module_name)
        if rel_path:
            targets.add(rel_path)

    if isinstance(node, ast.ImportFrom):
        module_name = resolve_importfrom_module_name(node, source_rel_path)
        add_candidate(module_name)
        for alias in node.names:
            if alias.name == "*":
                continue
            add_candidate(".".join(part for part in [module_name, alias.name] if part))
        return targets

    if isinstance(node, ast.Import):
        for alias in node.names:
            add_candidate(alias.name)
    return targets


def discover_internal_file_edges(src_dir: Path, system_id: str, all_py_files: list[str]) -> list[dict]:
    module_lookup = build_python_module_lookup(all_py_files)
    edge_counts: dict[tuple[str, str], int] = defaultdict(int)

    for rel_path in all_py_files:
        full_path = src_dir / rel_path
        try:
            source = full_path.read_text(encoding="utf-8", errors="replace")
            tree = ast.parse(source)
        except Exception:
            continue

        for node in ast.walk(tree):
            for target_rel_path in resolve_internal_import_file_targets(
                node,
                rel_path,
                system_id,
                module_lookup,
            ):
                if target_rel_path == rel_path:
                    continue
                edge_counts[(rel_path, target_rel_path)] += 1

    return [
        {"from": source_path, "to": target_path, "count": count}
        for (source_path, target_path), count in sorted(edge_counts.items())
    ]


def discover_system_files(src_dir):
    """Walk src/ and discover all .py and .md files per system directory."""
    systems = {}
    root_files = []

    for entry in sorted(src_dir.iterdir()):
        if entry.name.startswith("_") or entry.name.startswith("."):
            if entry.is_file():
                root_files.append(str(entry.relative_to(src_dir)))
            continue
        if entry.is_file():
            root_files.append(str(entry.relative_to(src_dir)))
            continue
        if not entry.is_dir():
            continue
        if any(excl in entry.name for excl in EXCLUDED_DIRS):
            continue

        system_id = entry.name
        py_files = sorted(str(f.relative_to(src_dir)) for f in entry.rglob("*.py"))
        md_files = sorted(str(f.relative_to(src_dir)) for f in entry.rglob("*.md"))

        agents = []
        agents_dir = entry / "agents"
        if agents_dir.exists():
            for af in sorted(agents_dir.glob("*.md")):
                metadata = parse_agent_frontmatter(af)
                agents.append({
                    "name": af.stem,
                    "path": str(af.relative_to(src_dir)),
                    "description": metadata.get("description") or af.stem,
                    "model": metadata.get("model") or "",
                })

        modules = discover_system_modules(src_dir, system_id, entry, agents)

        systems[system_id] = {
            "py_files": py_files,
            "md_files": md_files,
            "all_files": py_files + md_files,
            "agents": agents,
            "modules": modules,
            "internal_edges": discover_internal_edges(src_dir, system_id, modules),
            "internal_file_edges": discover_internal_file_edges(src_dir, system_id, py_files),
        }

    return systems, root_files


