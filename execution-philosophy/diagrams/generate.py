def run_agent(prompt_text, model=DEFAULT_MODEL, agent_file=None, timeout=180):
    """Run an LLM agent via the `agents` binary. Returns stdout."""
    PROMPTS_DIR.mkdir(exist_ok=True)
    prompt_path = PROMPTS_DIR / f"prompt-{os.getpid()}-{id(prompt_text) % 10000}.md"
    prompt_path.write_text(prompt_text, encoding="utf-8")

    cmd = ["agents", "--model", model, "--file", str(prompt_path)]
    if agent_file:
        cmd.extend(["--agent-file", str(agent_file)])

    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
            cwd=str(PROJECT_ROOT),
        )
        return result.stdout.strip()
    except subprocess.TimeoutExpired:
        print(f"  TIMEOUT after {timeout}s")
        return ""
    except Exception as exc:
        print(f"  ERROR: {exc}")
        return ""
    finally:
        prompt_path.unlink(missing_ok=True)


ANSI_RE = re.compile(r"\x1b\[[0-9;]*[a-zA-Z]")


def parse_json_response(text):
    """Extract a JSON object from LLM output (may contain fences or extra text)."""
    if not text:
        return None
    text = ANSI_RE.sub("", text)
    try:
        return json.loads(text.strip())
    except Exception:
        pass
    match = re.search(r"```(?:json)?\s*\n(.*?)\n```", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(1))
        except Exception:
            pass
    for pattern in [r'\{\s*"description"', r'\{\s*"label"']:
        candidates = []
        for found in re.finditer(pattern, text):
            start = found.start()
            depth = 0
            for idx in range(start, len(text)):
                if text[idx] == "{":
                    depth += 1
                elif text[idx] == "}":
                    depth -= 1
                    if depth == 0:
                        block = text[start:idx + 1]
                        try:
                            candidates.append(json.loads(block))
                            break
                        except Exception:
                            pass
                        collapsed = re.sub(r"\s+", " ", block)
                        try:
                            candidates.append(json.loads(collapsed))
                        except Exception:
                            pass
                        break
        if candidates:
            return candidates[-1]
    return None


