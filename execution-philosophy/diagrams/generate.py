def top_file_symbols(symbols: list[dict], rel_path: str, limit: int = 3) -> list[str]:
    ordered = sorted(
        [
            sym for sym in symbols
            if sym.get("file") == rel_path and sym.get("type") in {"class", "function"}
        ],
        key=lambda sym: (sym.get("line", 0), sym.get("name", "")),
    )
    names: list[str] = []
    seen: set[str] = set()
    for sym in ordered:
        name = sym.get("name", "")
        if not name or name in seen:
            continue
        names.append(name)
        seen.add(name)
        if len(names) >= limit:
            break
    return names


def diagram_node_label(rel_path: str, *, symbols: list[dict], summary: str = "") -> str:
    file_name = Path(rel_path).name
    if Path(rel_path).suffix == ".md":
        detail = summary or "Agent definition"
    else:
        top_symbols = top_file_symbols(symbols, rel_path)
        total_top_level = sum(
            1
            for sym in symbols
            if sym.get("file") == rel_path and sym.get("type") in {"class", "function"}
        )
        detail = " · ".join(top_symbols) if top_symbols else "Python implementation"
        if total_top_level > len(top_symbols):
            detail += " · ..."
    return "\n".join(part for part in (file_name, detail) if part)


