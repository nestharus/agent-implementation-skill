def discover_edges(src_dir, system_ids):
    """Discover cross-system edges by parsing import statements."""
    edge_map = defaultdict(lambda: {"source_files": set(), "imports": []})

    for sid in system_ids:
        sys_dir = src_dir / sid
        for py_file in sys_dir.rglob("*.py"):
            try:
                source = py_file.read_text(encoding="utf-8", errors="replace")
                tree = ast.parse(source)
            except Exception:
                continue

            rel = str(py_file.relative_to(src_dir))
            for node in ast.walk(tree):
                if isinstance(node, ast.ImportFrom) and node.module:
                    parts = node.module.split(".")
                    target = parts[0]
                    if target != sid and target in system_ids:
                        key = (sid, target)
                        edge_map[key]["source_files"].add(rel)
                        names = [a.name for a in (node.names or [])]
                        edge_map[key]["imports"].append({
                            "file": rel,
                            "line": node.lineno,
                            "module": node.module,
                            "names": names,
                        })

    edges = []
    for (from_sys, to_sys), data in sorted(edge_map.items()):
        edges.append({
            "from": from_sys,
            "to": to_sys,
            "source_files": sorted(data["source_files"]),
            "imports": data["imports"],
        })
    return edges


def _string_literal(node):
    if isinstance(node, ast.Constant) and isinstance(node.value, str):
        return node.value
    return None


def _call_name(node):
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        return node.attr
    return None


def _keyword_literal(call, key):
    for kw in call.keywords:
        if kw.arg == key:
            return _string_literal(kw.value)
    return None


def _discover_taskrouter_namespace(tree):
    for node in ast.walk(tree):
        if isinstance(node, ast.Call) and _call_name(node.func) == "TaskRouter" and node.args:
            namespace = _string_literal(node.args[0])
            if namespace:
                return namespace
    return None


def _caller_system(rel_path, system_ids):
    parts = Path(rel_path).parts
    if parts and parts[0] in system_ids:
        return parts[0]
    return "root"


def _agent_owner_index(src_dir, system_ids):
    owners = {}
    for sid in sorted(system_ids):
        agents_dir = src_dir / sid / "agents"
        if not agents_dir.exists():
            continue
        for agent_file in agents_dir.glob("*.md"):
            owners.setdefault(agent_file.name, sid)
    return owners


def _extract_callsite_snippet(py_path, line_no, context_lines=25):
    try:
        lines = py_path.read_text(encoding="utf-8", errors="replace").splitlines()
    except OSError:
        return ""
    if not lines:
        return ""
    if not line_no:
        start, end = 1, min(len(lines), context_lines * 2)
    else:
        start = max(1, line_no - context_lines)
        end = min(len(lines), line_no + context_lines)
    return "\n".join(lines[start - 1:end])


def discover_agent_routes(src_dir, system_ids):
    """Parse TaskRouter route registrations and index them by task type and agent."""
    route_index = {}
    agent_routes = defaultdict(list)

    for sid in sorted(system_ids):
        routes_path = src_dir / sid / "routes.py"
        if not routes_path.exists():
            continue
        try:
            source = routes_path.read_text(encoding="utf-8", errors="replace")
            tree = ast.parse(source)
        except Exception:
            continue

        namespace = _discover_taskrouter_namespace(tree)
        if not namespace:
            continue

        rel_path = str(routes_path.relative_to(src_dir))
        for node in ast.walk(tree):
            if not isinstance(node, ast.Call) or not isinstance(node.func, ast.Attribute):
                continue
            if node.func.attr != "route" or not node.args:
                continue

            task_name = _string_literal(node.args[0])
            agent_file = _keyword_literal(node, "agent")
            if not task_name or not agent_file:
                continue

            agent_basename = Path(agent_file).name
            model_name = _keyword_literal(node, "model") or ""
            policy_key = _keyword_literal(node, "policy_key") or ""
            task_type = f"{namespace}.{task_name}"
            route_entry = {
                "agent": agent_basename,
                "model": model_name,
                "policy_key": policy_key,
                "system": namespace,
                "task": task_name,
                "task_type": task_type,
                "route_file": rel_path,
                "line": node.lineno,
            }
            route_index[task_type] = route_entry
            agent_routes[agent_basename].append(route_entry)

    return route_index, dict(agent_routes)


def discover_agent_invocations(src_dir, system_ids, route_index):
    """Find agent callsites keyed by agent file.

    Detects:
    - agent_for("task.type") — direct agent resolution
    - resolve_agent_path("path/to/agent.md") — path-based resolution
    - TaskSpec(task_type="task.type") / TaskSpec("task.type") — task dispatch
    - Task(task_type="task.type") / Task("task.type") — task dispatch
    """
    agent_invocations = defaultdict(list)
    agent_owner_index = _agent_owner_index(src_dir, system_ids)
    _TASK_DISPATCH_NAMES = {"TaskSpec", "Task"}

    for py_file in sorted(src_dir.rglob("*.py")):
        try:
            source = py_file.read_text(encoding="utf-8", errors="replace")
            tree = ast.parse(source)
        except Exception:
            continue

        rel_path = str(py_file.relative_to(src_dir))
        caller = _caller_system(rel_path, system_ids)

        for node in ast.walk(tree):
            if not isinstance(node, ast.Call):
                continue

            call_name = _call_name(node.func)

            # --- TaskSpec / Task dispatch ---
            if call_name in _TASK_DISPATCH_NAMES:
                # task_type can be first positional arg or keyword
                task_type = None
                if node.args:
                    task_type = _string_literal(node.args[0])
                if not task_type:
                    task_type = _keyword_literal(node, "task_type")
                if not task_type:
                    continue
                route = route_index.get(task_type)
                if not route:
                    continue
                agent_invocations[route["agent"]].append({
                    "caller_file": rel_path,
                    "caller_system": caller,
                    "task_type": task_type,
                    "cross_system": caller != route["system"],
                    "invocation_type": "task_dispatch",
                    "line": node.lineno,
                    "target_system": route["system"],
                })
                continue

            # --- agent_for / resolve_agent_path (require positional arg) ---
            if not node.args:
                continue
            literal = _string_literal(node.args[0])
            if not literal:
                continue

            if call_name == "agent_for":
                route = route_index.get(literal)
                if not route:
                    continue
                agent_invocations[route["agent"]].append({
                    "caller_file": rel_path,
                    "caller_system": caller,
                    "task_type": literal,
                    "cross_system": caller != route["system"],
                    "invocation_type": "agent_for",
                    "line": node.lineno,
                    "target_system": route["system"],
                })
            elif call_name == "resolve_agent_path":
                agent_basename = Path(literal).name
                owner_system = agent_owner_index.get(agent_basename)
                if not owner_system:
                    continue
                agent_invocations[agent_basename].append({
                    "caller_file": rel_path,
                    "caller_system": caller,
                    "task_type": "",
                    "cross_system": caller != owner_system,
                    "invocation_type": "resolve_agent_path",
                    "line": node.lineno,
                    "target_system": owner_system,
                })

    return dict(agent_invocations)


