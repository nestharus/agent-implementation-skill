def file_hash(paths):
    """SHA-256 of concatenated file contents, truncated to 16 hex chars."""
    h = hashlib.sha256()
    for p in sorted(paths):
        try:
            h.update(Path(p).read_bytes())
        except OSError:
            pass
    return h.hexdigest()[:16]


