def build_adjacency(system_ids, edges):
    adj = defaultdict(lambda: defaultdict(float))
    for edge in edges:
        src, dst = edge["from"], edge["to"]
        if src in system_ids and dst in system_ids:
            adj[src][dst] += 1.0
            adj[dst][src] += 1.0
    return adj


def modularity_gain(adj, node, target_comm, communities, node_comm, m):
    ki = sum(adj[node][j] for j in adj[node])
    ki_in = sum(adj[node].get(j, 0.0) for j in communities[target_comm])
    sigma_tot = sum(sum(adj[n][j] for j in adj[n]) for n in communities[target_comm])
    if m == 0:
        return 0.0
    return (ki_in / m) - (sigma_tot * ki) / (2.0 * m * m)


def greedy_modularity_clustering(system_ids, edges):
    adj = build_adjacency(system_ids, edges)
    m = sum(adj[i][j] for i in system_ids for j in adj[i]) / 2.0
    if m == 0:
        return {sid: {sid} for sid in system_ids}

    communities = {sid: {sid} for sid in system_ids}
    node_comm = {sid: sid for sid in system_ids}
    improved = True
    iters = 0

    while improved and iters < 100:
        improved = False
        iters += 1
        nodes = list(system_ids)
        random.seed(42 + iters)
        random.shuffle(nodes)

        for node in nodes:
            current = node_comm[node]
            best_comm, best_gain = current, 0.0
            tried = set()

            for neighbor in adj[node]:
                nc = node_comm[neighbor]
                if nc == current or nc in tried:
                    continue
                tried.add(nc)
                communities[current].discard(node)
                gain = modularity_gain(adj, node, nc, communities, node_comm, m)
                loss = -modularity_gain(adj, node, current, communities, node_comm, m)
                communities[current].add(node)
                if gain + loss > best_gain:
                    best_gain = gain + loss
                    best_comm = nc

            if best_comm != current:
                communities[current].discard(node)
                if not communities[current]:
                    del communities[current]
                communities[best_comm].add(node)
                node_comm[node] = best_comm
                improved = True

    return {key: value for key, value in communities.items() if value}


