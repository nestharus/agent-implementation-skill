def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--model", default=DEFAULT_MODEL)
    parser.add_argument("--no-cache", action="store_true")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--serve", action="store_true")
    parser.add_argument("--port", type=int, default=8742)
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(sys.argv[1:] if argv is None else argv)

    print("Phase 1: Discovering source files...")
    discovered, root_files = discover_system_files(SRC_DIR)
    system_ids = list(discovered.keys())
    total_files = sum(len(data["all_files"]) for data in discovered.values())
    print(f"  {len(system_ids)} systems, {total_files} files")

    print("Phase 1b: Discovering agent routes...")
    route_index, agent_routes = discover_agent_routes(SRC_DIR, set(system_ids))
    print(f"  {len(route_index)} routes, {len(agent_routes)} unique agent files")

    print("Phase 1c: Discovering agent invocations...")
    agent_invocations = discover_agent_invocations(SRC_DIR, set(system_ids), route_index)
    total_invocations = sum(len(v) for v in agent_invocations.values())
    print(f"  {total_invocations} invocations across {len(agent_invocations)} agents")

    print("Phase 2: Discovering edges from imports...")
    raw_edges = discover_edges(SRC_DIR, set(system_ids))
    print(f"  {len(raw_edges)} cross-system edges")

    if args.dry_run:
        print("\n=== DRY RUN ===")
        print(f"Would generate {len(system_ids)} system descriptions")
        print(f"Would generate {len(raw_edges)} edge descriptions")
        total_agents = sum(len(data.get("agents", [])) for data in discovered.values())
        print(f"Would generate {total_agents} agent descriptions")
        for sid in sorted(system_ids):
            print(f"  system: {sid} ({len(discovered[sid]['all_files'])} files)")
        for edge in raw_edges:
            print(f"  edge: {edge['from']} → {edge['to']} ({len(edge['imports'])} imports)")
        for sid in sorted(system_ids):
            for agent in discovered[sid].get("agents", []):
                agent_name = agent["name"]
                agent_basename = f"{agent_name}.md"
                print(
                    f"  agent: {sid}.{agent_name} "
                    f"({len(agent_routes.get(agent_basename, []))} routes, "
                    f"{len(agent_invocations.get(agent_basename, []))} invocations)"
                )
        return 0

    if not agents_available() and args.no_cache:
        print("ERROR: `agents` binary is not available and --no-cache was requested.")
        return 1

    if not agents_available():
        print("WARNING: `agents` binary not found. Using cached content where available; uncached items fall back to structure-only pages.")

    sys_content, edge_content, agent_content = generate_all_content(
        discovered,
        raw_edges,
        SRC_DIR,
        args.model,
        args.no_cache,
        MAX_WORKERS,
        agent_routes,
        agent_invocations,
    )

    print("Phase 5: Clustering...")
    raw_clusters = greedy_modularity_clustering(system_ids, raw_edges)
    named_clusters = name_clusters(raw_clusters, system_ids)
    for name, members in sorted(named_clusters.items()):
        print(f"  {name}: {sorted(members)}")

    print("Phase 6: Synthesizing behavioral lifecycle data...")
    behavioral_synthesis = generate_behavioral_synthesis(
        discovered,
        named_clusters,
        sys_content,
        agent_routes,
        agent_invocations,
        agent_content,
        model=args.model,
        no_cache=args.no_cache,
    )

    print("Phase 7: Building workspace model...")
    workspace = build_workspace_model(
        discovered,
        root_files,
        named_clusters,
        raw_edges,
        sys_content,
        edge_content,
        agent_routes,
        agent_invocations,
        agent_content,
        behavioral_synthesis=behavioral_synthesis,
    )

    print("Phase 7b: Validating behavioral graph...")
    validation_report = library_validate_graph(workspace)
    validation_status = validation_report.get("status", "ok")
    summary = validation_report.get("summary", {})
    print(f"  Status: {validation_status} ({summary.get('error_count', 0)} errors, {summary.get('warning_count', 0)} warnings)")

    print("Phase 8: Writing site...")
    site = library_generate_site(workspace, output_dir=SITE_DIR)

    print("Phase 8b: Writing codemap...")
    codemap_path = library_write_codemap(workspace, DIAG_DIR)
    print(f"  Wrote {codemap_path}")

    print("Phase 8c: Writing lifemap...")
    lifemap_path = library_write_lifemap(workspace, DIAG_DIR)
    print(f"  Wrote {lifemap_path}")

    print("Phase 8d: Writing validation report...")
    validation_report_path = DIAG_DIR / "validation-report.json"
    write_text(validation_report_path, json.dumps(validation_report, indent=2, sort_keys=True))
    print(f"  Wrote {validation_report_path}")

    print("Phase 8e: Writing workspace manifest...")
    workspace_path = write_workspace_model(workspace, DIAG_DIR)
    print(f"  Wrote {workspace_path}")

    coverage = workspace.metadata.get("coverage", site["coverage"])
    print(f"\nFiles: {coverage['coveredFiles']}/{coverage['totalFiles']} ({coverage['percentage']}%)")
    print(f"Symbols: {coverage['referencedSymbols']}/{coverage['totalSymbols']} ({coverage['symbolPercentage']}%)")

    if args.serve:
        os.chdir(DIAG_DIR)
        server = http.server.HTTPServer(("", args.port), http.server.SimpleHTTPRequestHandler)
        print(f"\nServing at http://localhost:{args.port}/site/")
        webbrowser.open(f"http://localhost:{args.port}/site/")
        try:
            server.serve_forever()
        except KeyboardInterrupt:
            server.shutdown()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
