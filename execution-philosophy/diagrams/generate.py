def extract_symbols(py_file_path):
    """Extract public classes and top-level functions from a Python file."""
    try:
        source = Path(py_file_path).read_text(encoding="utf-8", errors="replace")
        tree = ast.parse(source)
    except Exception:
        return []

    symbols = []
    rel = str(py_file_path)
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, ast.ClassDef) and not node.name.startswith("_"):
            symbols.append({"type": "class", "name": node.name, "line": node.lineno, "file": rel})
            for child in ast.iter_child_nodes(node):
                if isinstance(child, (ast.FunctionDef, ast.AsyncFunctionDef)) and not child.name.startswith("_"):
                    symbols.append({"type": "method", "name": f"{node.name}.{child.name}", "line": child.lineno, "file": rel})
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and not node.name.startswith("_"):
            symbols.append({"type": "function", "name": node.name, "line": node.lineno, "file": rel})
    return symbols


def compute_symbol_coverage(description_corpus, symbols):
    """Heuristic: check if symbol names appear in the description text."""
    corpus_words = set(re.findall(r"[a-z]{3,}", description_corpus.lower()))
    for sym in symbols:
        raw = sym["name"].split(".")[-1]
        words = set(re.findall(r"[a-z]{3,}", raw.lower()))
        sym["referenced"] = bool(words & corpus_words) if words else False
    total = len(symbols)
    referenced = sum(1 for s in symbols if s.get("referenced"))
    return {"total": total, "referenced": referenced}


