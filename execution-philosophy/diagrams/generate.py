def build_system_prompt(sid, system_data, src_dir):
    """Build a prompt for the system-describer agent."""
    parts = [f"# System: {sid}", f"Directory: src/{sid}/", ""]

    parts.append("## Files")
    for f in system_data["all_files"]:
        parts.append(f"- {f}")
    parts.append("")

    if system_data.get("agents"):
        parts.append("## Agent Definitions")
        for agent in system_data["agents"]:
            parts.append(f"- **{agent['name']}**: {agent['description']}")
        parts.append("")

    parts.append("## Source Code")
    budget = MAX_PROMPT_CHARS
    used = 0

    priority = ["routes.py", "types.py"]
    py_files = system_data.get("py_files", [])
    pri = [f for f in py_files if any(f.endswith(p) for p in priority)]
    rest = [f for f in py_files if f not in pri and not f.endswith("__init__.py")]

    for f in pri + rest:
        full = src_dir / f
        if not full.exists():
            continue
        content = full.read_text(encoding="utf-8", errors="replace")
        if used + len(content) > budget:
            sigs = _extract_signatures(full)
            if sigs:
                parts.extend([f"### {f} (signatures only)", "```python", sigs, "```", ""])
                used += len(sigs)
        else:
            parts.extend([f"### {f}", "```python", content, "```", ""])
            used += len(content)

    return "\n".join(parts)


def build_edge_prompt(edge, systems, src_dir):
    """Build a prompt for the edge-describer agent."""
    from_sys = edge["from"]
    to_sys = edge["to"]
    parts = [
        f"# Connection: {from_sys} → {to_sys}",
        f"System {from_sys} imports from system {to_sys}.",
        "",
        "## Import Statements",
    ]

    for imp in edge["imports"][:20]:
        names_str = ", ".join(imp["names"][:5])
        parts.append(f"- In `{imp['file']}`: `from {imp['module']} import {names_str}`")
    parts.append("")

    budget = MAX_PROMPT_CHARS // 2
    used = 0
    parts.append(f"## Source code from {from_sys} (importer)")
    for f in edge["source_files"][:3]:
        full = src_dir / f
        if not full.exists():
            continue
        content = full.read_text(encoding="utf-8", errors="replace")
        if used + len(content) > budget:
            content = content[:budget - used] + "\n# ... truncated"
        parts.extend([f"### {f}", "```python", content, "```", ""])
        used += len(content)
        if used >= budget:
            break

    parts.append(f"## Key imported code from {to_sys}")
    seen = set()
    used = 0
    for imp in edge["imports"][:10]:
        mod_path = src_dir / imp["module"].replace(".", "/") / "__init__.py"
        if not mod_path.exists():
            mod_path = src_dir / (imp["module"].replace(".", "/") + ".py")
        if mod_path.exists() and str(mod_path) not in seen:
            seen.add(str(mod_path))
            content = mod_path.read_text(encoding="utf-8", errors="replace")
            rel = str(mod_path.relative_to(src_dir))
            if used + len(content) > budget:
                content = content[:budget - used] + "\n# ... truncated"
            parts.extend([f"### {rel}", "```python", content, "```", ""])
            used += len(content)
            if used >= budget:
                break

    return "\n".join(parts)


def build_agent_prompt(agent, owner_system, route_entries, invocations, src_dir):
    """Build a prompt for the agent-describer agent."""
    agent_name = agent["name"]
    agent_rel_path = agent.get("path") or f"{owner_system}/agents/{agent_name}.md"
    agent_path = src_dir / agent_rel_path
    agent_definition = agent_path.read_text(encoding="utf-8", errors="replace")

    parts = [
        f"# Agent: {agent_name}",
        f"System: {owner_system}",
        f"File: src/{agent_rel_path}",
        "",
        "## Agent Definition",
        agent_definition,
        "",
        "## Route Registrations",
    ]
    used = len("\n".join(parts))

    if route_entries:
        for route in sorted(route_entries, key=lambda item: item["task_type"]):
            line = f"- {route['task_type']}: model={route['model'] or '(policy/default)'}"
            if route.get("policy_key"):
                line += f", policy_key={route['policy_key']}"
            parts.append(line)
    else:
        parts.append("- None found")
    parts.append("")

    parts.append("## Invocation Callsites")
    used = len("\n".join(parts))
    primary_invocations = [inv for inv in invocations if inv.get("invocation_type") == "agent_for"]
    selected_invocations = primary_invocations or [
        inv for inv in invocations if inv.get("invocation_type") == "resolve_agent_path"
    ]

    if not selected_invocations:
        parts.append("- None found")
        return "\n".join(parts)

    for inv in sorted(selected_invocations, key=lambda item: (item["caller_file"], item.get("line", 0)))[:3]:
        snippet = _extract_callsite_snippet(src_dir / inv["caller_file"], inv.get("line"))
        block = "\n".join([
            f"### {inv['caller_file']} (line ~{inv.get('line', '?')})",
            "```python",
            snippet,
            "```",
            "",
        ])
        if used + len(block) > MAX_PROMPT_CHARS:
            break
        parts.append(block.rstrip())
        parts.append("")
        used += len(block)

    return "\n".join(parts).strip()


def _extract_signatures(py_path):
    """Extract class/function signatures from a Python file (no bodies)."""
    try:
        source = py_path.read_text(encoding="utf-8", errors="replace")
        tree = ast.parse(source)
    except Exception:
        return ""

    lines = []
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, ast.ClassDef):
            lines.append(f"class {node.name}:")
            for child in ast.iter_child_nodes(node):
                if isinstance(child, (ast.FunctionDef, ast.AsyncFunctionDef)):
                    args = ast.get_source_segment(source, child.args) or "..."
                    prefix = "async def " if isinstance(child, ast.AsyncFunctionDef) else "def "
                    lines.append(f"    {prefix}{child.name}({args}): ...")
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            prefix = "async def " if isinstance(node, ast.AsyncFunctionDef) else "def "
            args = ast.get_source_segment(source, node.args) or "..."
            lines.append(f"{prefix}{node.name}({args}): ...")
    return "\n".join(lines)


