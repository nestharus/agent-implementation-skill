def build_cluster_positions(cluster_ids: list[str]) -> dict[str, dict]:
    preferred = {
        "discover": (1, 1),
        "frame": (1, 2),
        "plan": (1, 3),
        "integrate": (2, 1),
        "validate": (2, 2),
        "execute": (2, 3),
        "infrastructure": (3, 2),
        "support": (4, 1),
    }
    positions = {}
    occupied = set()

    for cluster_id in cluster_ids:
        if cluster_id in preferred:
            positions[cluster_id] = {"row": preferred[cluster_id][0], "col": preferred[cluster_id][1]}
            occupied.add(preferred[cluster_id])

    extras = [cid for cid in cluster_ids if cid not in positions]
    row, col = 4, 2
    for cluster_id in extras:
        while (row, col) in occupied:
            col += 1
            if col > CANVAS_COLUMNS:
                row += 1
                col = 1
        positions[cluster_id] = {"row": row, "col": col}
        occupied.add((row, col))
        col += 1
        if col > CANVAS_COLUMNS:
            row += 1
            col = 1
    return positions


def cluster_box(position: dict) -> dict[str, float]:
    x = CANVAS_PADDING + (position["col"] - 1) * (CARD_WIDTH + CARD_GAP_X)
    y = CANVAS_PADDING + (position["row"] - 1) * (CARD_HEIGHT + CARD_GAP_Y)
    return {
        "x": x,
        "y": y,
        "w": CARD_WIDTH,
        "h": CARD_HEIGHT,
        "cx": x + CARD_WIDTH / 2,
        "cy": y + CARD_HEIGHT / 2,
    }


def arrow_endpoint(source_box: dict, target_box: dict) -> tuple[tuple[float, float], tuple[float, float]]:
    dx = target_box["cx"] - source_box["cx"]
    dy = target_box["cy"] - source_box["cy"]
    if abs(dx) >= abs(dy):
        if dx >= 0:
            return (
                (source_box["x"] + source_box["w"], source_box["cy"]),
                (target_box["x"], target_box["cy"]),
            )
        return (
            (source_box["x"], source_box["cy"]),
            (target_box["x"] + target_box["w"], target_box["cy"]),
        )
    if dy >= 0:
        return (
            (source_box["cx"], source_box["y"] + source_box["h"]),
            (target_box["cx"], target_box["y"]),
        )
    return (
        (source_box["cx"], source_box["y"]),
        (target_box["cx"], target_box["y"] + target_box["h"]),
    )


def bezier_path(start: tuple[float, float], end: tuple[float, float]) -> str:
    sx, sy = start
    ex, ey = end
    dx = ex - sx
    dy = ey - sy
    if abs(dx) >= abs(dy):
        c1 = (sx + dx * 0.38, sy)
        c2 = (ex - dx * 0.38, ey)
    else:
        c1 = (sx, sy + dy * 0.38)
        c2 = (ex, ey - dy * 0.38)
    return f"M {sx:.1f} {sy:.1f} C {c1[0]:.1f} {c1[1]:.1f}, {c2[0]:.1f} {c2[1]:.1f}, {ex:.1f} {ey:.1f}"


def arrow_polygon(start: tuple[float, float], end: tuple[float, float], size: float = 7.0) -> str:
    sx, sy = start
    ex, ey = end
    dx = ex - sx
    dy = ey - sy
    length = max((dx * dx + dy * dy) ** 0.5, 1.0)
    ux, uy = dx / length, dy / length
    px, py = -uy, ux
    left = (ex - ux * size - px * (size * 0.55), ey - uy * size - py * (size * 0.55))
    right = (ex - ux * size + px * (size * 0.55), ey - uy * size + py * (size * 0.55))
    return f"{ex:.1f},{ey:.1f} {left[0]:.1f},{left[1]:.1f} {right[0]:.1f},{right[1]:.1f}"


def compute_canvas_metrics(positions: dict[str, dict]) -> dict[str, int]:
    max_row = max((value["row"] for value in positions.values()), default=1)
    width = int(CANVAS_PADDING * 2 + CANVAS_COLUMNS * CARD_WIDTH + (CANVAS_COLUMNS - 1) * CARD_GAP_X)
    height = int(CANVAS_PADDING * 2 + max_row * CARD_HEIGHT + (max_row - 1) * CARD_GAP_Y)
    return {"width": width, "height": height}


def render_cluster_arrows(cluster_edges: list[dict], positions: dict[str, dict]) -> str:
    items = []
    for edge in cluster_edges:
        source = positions.get(edge["from"])
        target = positions.get(edge["to"])
        if not source or not target:
            continue
        source_box = cluster_box(source)
        target_box = cluster_box(target)
        start, end = arrow_endpoint(source_box, target_box)
        path = bezier_path(start, end)
        label_x = (start[0] + end[0]) / 2
        label_y = (start[1] + end[1]) / 2 - 8
        items.append(
            textwrap.dedent(
                f"""
                <g class="arrow" data-source-cluster="{escape(edge["from"])}" data-target-cluster="{escape(edge["to"])}">
                    <path d="{path}"></path>
                    <polygon points="{arrow_polygon(start, end)}"></polygon>
                    <text x="{label_x:.1f}" y="{label_y:.1f}" text-anchor="middle">{escape(edge["label"])}</text>
                </g>
                """
            ).strip()
        )
    return "\n".join(items)


