def resolve_artifact_store_ids(label: str, stores: list[dict]) -> list[str]:
    wanted = normalized_store_key(label)
    if not wanted:
        return []
    matched: list[str] = []
    for store in stores:
        candidates = {
            normalized_store_key(store.get("id", "")),
            normalized_store_key(store.get("label", "")),
            normalized_store_key(store.get("location", "")),
        }
        candidates.update(normalized_store_key(table) for table in store.get("tables", []))
        if wanted in candidates or any(wanted and wanted in candidate for candidate in candidates if candidate):
            matched.append(store["id"])
    return matched


def find_symbol_span(rel_path: str, line: int | None = None) -> tuple[int, int, str]:
    full_path = SRC_DIR / rel_path
    try:
        content = full_path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return (line or 1, line or 1, "")

    lines = content.splitlines()
    if full_path.suffix != ".py":
        line_start = max(1, line or 1)
        line_end = min(len(lines) or line_start, line_start + 24)
        return (line_start, line_end, "")

    try:
        tree = ast.parse(content)
    except Exception:
        line_start = max(1, line or 1)
        line_end = min(len(lines) or line_start, line_start + 24)
        return (line_start, line_end, "")

    match_symbol = ""
    match_start = 1
    match_end = min(len(lines), 40) if lines else 1
    target_line = line or 1

    def consider(node: ast.AST, symbol_name: str) -> None:
        nonlocal match_symbol, match_start, match_end
        start = getattr(node, "lineno", None)
        end = getattr(node, "end_lineno", None)
        if start is None or end is None:
            return
        if start <= target_line <= end:
            if not match_symbol or (end - start) < (match_end - match_start):
                match_symbol = symbol_name
                match_start = start
                match_end = end

    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef):
            consider(node, node.name)
            for child in node.body:
                if isinstance(child, (ast.FunctionDef, ast.AsyncFunctionDef)):
                    consider(child, f"{node.name}.{child.name}")
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            consider(node, node.name)

    if match_symbol:
        return (match_start, match_end, match_symbol)

    if line:
        line_start = max(1, line - 6)
        line_end = min(len(lines) or line, line + 10)
        return (line_start, line_end, "")
    return (1, min(len(lines), 40) if lines else 1, "")


def anchor_code_block(
    code_blocks: dict[str, CodeBlock],
    step_to_code_ids: dict[str, list[str]],
    *,
    rel_path: str,
    component_id: str,
    step_id: str,
    line: int | None = None,
    symbol: str = "",
) -> str:
    line_start, line_end, discovered_symbol = find_symbol_span(rel_path, line)
    effective_symbol = symbol or discovered_symbol
    block_id = "code:" + behavioral_slug(f"{rel_path}:{line_start}:{line_end}:{effective_symbol or component_id}")
    block = code_blocks.get(block_id)
    if block is None:
        block = CodeBlock(
            id=block_id,
            path=f"src/{rel_path}",
            line_start=line_start,
            line_end=line_end,
            symbol=effective_symbol,
            language="markdown" if rel_path.endswith(".md") else "python",
            component_id=component_id,
            step_ids=[step_id],
        )
        code_blocks[block_id] = block
    elif step_id not in block.step_ids:
        block.step_ids.append(step_id)
    step_to_code_ids.setdefault(step_id, [])
    if block_id not in step_to_code_ids[step_id]:
        step_to_code_ids[step_id].append(block_id)
    return block_id


def append_crossref(
    crossrefs: list[CrossReference],
    seen: set[tuple[str, str, str, str, str]],
    *,
    source_id: str,
    target_id: str,
    source_view: str,
    target_view: str,
    relationship: str,
) -> None:
    key = (source_id, target_id, source_view, target_view, relationship)
